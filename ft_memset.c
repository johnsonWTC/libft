#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*p;

	p = b;
	i = 0;
	while (len > 0)
	{
		p[i] = c;
		i++;
		len--;
	}
	return (b);
}
