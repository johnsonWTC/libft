#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s;
	int		len;

	len = 0;
	while (s1[len])
		len++;
	s = (char *)malloc(sizeof(char ) * (len + 1));
	if (s == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
