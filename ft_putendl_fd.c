#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char	*s1;
	int		i;

	if (s == 0)
		return ;
	s1 = (char *)s;
	i = 0;
	while (s1[i])
	{
		write(fd, &s1[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
