#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	int		i;
	char	*p;

	p = b;
	i = 0;
	while (len > 0)
	{
		p[i] = 0;
		i++;
		len--;
	}
	return (b);
}
