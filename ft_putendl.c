#include "libft.h"

void	ft_putendl(char const *s)
{
	char	*s1;
	int		i;

	if (s == 0)
		return ;
	s1 = (char *)s;
	i = 0;
	while (s1[i])
	{
		write(1, &s1[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
